package com.pswk.ehr.ed03;

public class Ed01For {

	public static void main(String[] args) {
		// 1~5까지 순차적으로 출력
		int i =1;
		
		System.out.println(i);
		System.out.println(++i);
		System.out.println(++i);
		System.out.println(++i);
		System.out.println(++i);
		System.out.println("-----------------");
		
		for(int j =1;j<=5;j++)
			System.out.println(j);
	}

}
