package com.pswk.ehr.ed03;

public class Ed08EnhancedFor {

	public static void main(String[] args) {
		// 향상된 for문은 배열과 컬렉션을 순회할 때 사용되는 편리한 반복

		int[] numbers = { 14, 16, 19, 49, 53 };

		for (int num : numbers) {
			System.out.print(num + ",");
		}
	}

}
