package com.pcwk.ehr.ed01;

public class Member {

		private String name;
		private int age;
		/**
		 * @param name
		 * @param age
		 */
		public Member(String name, int age) {
			super();
			this.name = name;
			this.age = age;
		}
		public String getName() {
			return name;
		}
		public int getAge() {
			return age;
		}
		

}
