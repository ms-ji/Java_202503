package com.pcwk.ehr.ed01;

public class Calculrator {
	//변수
	
	//생성자
	
	//메서드
	public Calculrator() {
		
		
	}
	/**
	 * 더하기: a+b
	 * @param a
	 * @param b
	 * @return
	 */
	public int add(int a, int b) {
		return a+b;
	}
	/**
	 * 더하기 : a+b+c
	 * @param a
	 * @param b
	 * @param c
	 * @return
	 */
	public int add(int a, int b, int c) {
		return a+b+c;
	}
	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public double add(double a, double b) {
		return a+b;
	}
}
