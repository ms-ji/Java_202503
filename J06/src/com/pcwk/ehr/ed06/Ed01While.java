package com.pcwk.ehr.ed06;

public class Ed01While {
	public static void main(String[] args) {
		int i =1;
		
		while(i<=5) {
			System.out.printf("i=%d%n",i);
			i++;
			
		}//while end
		System.out.println("프로그램 종료");
	}//main end

}
